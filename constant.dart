void main() {
  // defining a constant using final keyword

  // final const_name;  or
  // final data_type const_name;
  final a = 10;
  final int b = 20;

  print(a);
  print(b);

  // Defining a constant using const keyword

  // const const_name; or
  // const data_type const_name;

  const name = 'Satoru Gojo';
  const String charName = 'Yuta Okotsku';

  print(name);
  print(charName);
}
